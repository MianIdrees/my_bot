#!/usr/bin/env python3
"""
Relay node: subscribes to /cmd_vel (Twist) and republishes as
/diff_cont/cmd_vel (TwistStamped) so that standard teleop tools
work seamlessly with the ros2_control diff_drive_controller.
"""

import rclpy
from rclpy.node import Node
from geometry_msgs.msg import Twist, TwistStamped


class TwistRelay(Node):
    def __init__(self):
        super().__init__('twist_relay')
        self.sub = self.create_subscription(Twist, '/cmd_vel', self.callback, 10)
        self.pub = self.create_publisher(TwistStamped, '/diff_cont/cmd_vel', 10)
        self.get_logger().info('Twist relay active: /cmd_vel â†’ /diff_cont/cmd_vel')

    def callback(self, msg: Twist):
        stamped = TwistStamped()
        stamped.header.stamp = self.get_clock().now().to_msg()
        stamped.header.frame_id = 'base_link'
        stamped.twist = msg
        self.pub.publish(stamped)


def main():
    rclpy.init()
    node = TwistRelay()
    rclpy.spin(node)
    rclpy.shutdown()


if __name__ == '__main__':
    main()
